#!/usr/bin/env Rscript
# MAR Benchmark Visualization - Comprehensive & Clean
# Usage: Rscript plot_results.R [input.csv] [output_dir]

suppressPackageStartupMessages({
  library(tidyverse)
  library(scales)
})

args <- commandArgs(trailingOnly = TRUE)
input_file <- if (length(args) >= 1) args[1] else "results.csv"
output_dir <- if (length(args) >= 2) args[2] else "visuals"

if (!file.exists(input_file)) stop(paste("Input file not found:", input_file))
dir.create(output_dir, showWarnings = FALSE, recursive = TRUE)

cat("Reading results from:", input_file, "\n")
df_in <- read_csv(input_file, show_col_types = FALSE)

# ------------------------------------------------------------------------------
# Global plot annotations (cache mode, etc.)
# ------------------------------------------------------------------------------
cache_note <- ""
threads_note <- ""
if (all(c("dataset", "operation", "error") %in% names(df_in))) {
  cm <- df_in %>% filter(dataset == "_meta", operation == "cache_mode") %>% slice_head(n = 1)
  if (nrow(cm) == 1) {
    if (cm$error[[1]] == "cold") cache_note <- "\nCache: cold (purged before each run)"
    if (cm$error[[1]] == "warm") cache_note <- "\nCache: warm (OS file cache may help repeated runs)"
  }

  th <- df_in %>% filter(dataset == "_meta", operation == "threads") %>% slice_head(n = 1)
  if (nrow(th) == 1) {
    if (th$error[[1]] == "default") threads_note <- "\nThreads: default"
    if (th$error[[1]] != "default") threads_note <- paste0("\nThreads: ", th$error[[1]])
  }
}

run_note <- paste0(cache_note, threads_note)

# ------------------------------------------------------------------------------
# Input schema normalization
#
# We support two schemas:
#
# (A) "Legacy long" schema (generated by run_benchmarks.py):
#   workload, operation, variant, metric, value
#
# (B) "Easy runner" schema (generated by run_easy.py):
#   dataset, tool, compression, operation, seconds, input_bytes, output_bytes, status, ...
#
# This block converts (B) -> (A) so the rest of this script can stay simple.
# ------------------------------------------------------------------------------

if (!("variant" %in% names(df_in)) && all(c("dataset", "tool", "compression", "operation") %in% names(df_in))) {
  df_easy <- df_in %>%
    filter(is.na(status) | status == "ok") %>%
    mutate(
      workload = dataset,
      operation_raw = operation,
      operation = case_when(
        str_starts(operation_raw, "get_") ~ "get",
        str_starts(operation_raw, "cat_") ~ "cat",
        TRUE ~ operation_raw
      ),
      variant = case_when(
        tool == "tar" & compression == "none" ~ "tar_uncompressed",
        tool == "tar" & compression == "gzip" ~ "tar_gzip",
        tool == "mar" & compression == "zstd" ~ "mar_zstd",
        tool == "mar" & compression == "gzip" ~ "mar_gzip",
        tool == "mar" & compression == "lz4" ~ "mar_lz4",
        tool == "mar" & compression == "none" ~ "mar_uncompressed",
        TRUE ~ paste0(tool, "_", compression)
      ),
      seconds_num = suppressWarnings(as.numeric(seconds)),
      input_bytes_num = suppressWarnings(as.numeric(input_bytes)),
      output_bytes_num = suppressWarnings(as.numeric(output_bytes))
    )

  sizes <- df_easy %>%
    group_by(workload) %>%
    summarise(input_bytes = max(input_bytes_num, na.rm = TRUE), .groups = "drop") %>%
    mutate(
      input_bytes = ifelse(is.finite(input_bytes), input_bytes, NA_real_),
      workload_label = ifelse(is.na(input_bytes), workload, paste0(workload, " (", label_bytes()(input_bytes), ")"))
    ) %>%
    arrange(input_bytes)

  # time_real metric
  df_time <- df_easy %>%
    filter(!is.na(seconds_num)) %>%
    transmute(
      benchmark_id = NA_character_,
      workload,
      operation,
      variant,
      metric = "time_real",
      value = seconds_num,
      units = "seconds"
    )

  # compression_ratio metric (higher is better): raw_bytes / archive_bytes
  df_ratio <- df_easy %>%
    filter(operation == "create", !is.na(input_bytes_num), !is.na(output_bytes_num), output_bytes_num > 0) %>%
    transmute(
      benchmark_id = NA_character_,
      workload,
      operation,
      variant,
      metric = "compression_ratio",
      value = input_bytes_num / output_bytes_num,
      units = "ratio"
    )

  df_long <- bind_rows(df_time, df_ratio) %>% left_join(sizes, by = "workload")
} else {
  df_long <- df_in
}

# Drop meta/probe rows from plots
