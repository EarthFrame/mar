theme_tufte <- function(base_size = 12) {
  # A restrained theme inspired by Tufte + Wilke:
  # - maximize data-ink (light grids, no boxes)
  # - small multiples (facets) for comparison
  # - direct labels whenever possible
  theme_minimal(base_size = base_size) +
    theme(
      panel.grid.minor = element_blank(),
      panel.grid.major.x = element_blank(),   # bars/points already encode x
      panel.grid.major.y = element_line(color = "#e6e6e6", linewidth = 0.3),
      axis.line.x = element_line(color = "black"),
      axis.ticks.x = element_line(color = "black"),
      plot.title = element_text(face = "bold", size = 14),
      plot.subtitle = element_text(size = 10, color = "#666666"),
      strip.text = element_text(face = "bold", size = 11),
      legend.position = "bottom",
      panel.spacing = unit(1.2, "lines")
    )
}

fmt_seconds <- function(x) {
  case_when(
    is.na(x) ~ "",
    x < 0.001 ~ sprintf("%.0fµs", x * 1e6),
    x < 1 ~ sprintf("%.0fms", x * 1e3),
    x < 10 ~ sprintf("%.2fs", x),
    TRUE ~ sprintf("%.1fs", x)
  )
}

fmt_pct <- function(x) {
  ifelse(is.na(x), "", sprintf("%.0f%%", x))
}

# Lollipop (dot) chart helper: clearer than bars for precise reading.
plot_lollipop <- function(df, title, subtitle, xlab, out_path, vline = NULL, label_fn = NULL) {
  if (is.null(label_fn)) label_fn <- function(x) x
  if (!("facet_workload" %in% names(df))) {
    if ("workload_label" %in% names(df)) {
      df <- df %>% mutate(facet_workload = workload_label)
    } else if ("workload" %in% names(df)) {
      df <- df %>% mutate(facet_workload = workload)
    } else {
      df <- df %>% mutate(facet_workload = "workload")
    }
  }
  p <- ggplot(df, aes(x = value, y = display_label, color = display_label)) +
    geom_segment(aes(x = 0, xend = value, yend = display_label), linewidth = 0.8, alpha = 0.55) +
    geom_point(size = 3) +
    geom_text(aes(label = label_fn(value)), hjust = -0.12, size = 3, color = "#222222") +
    facet_wrap(~facet_workload, scales = "free_x") +
    scale_color_manual(
      values = c(
        "TAR (Uncompressed)" = "#6e6e6e",
        "TAR+Gzip" = "#3d3d3d",
        "MAR (Uncompressed)" = mar_none_color,
        "MAR+ZSTD" = mar_zstd_color,
        "MAR+ZSTD (no checksum)" = alpha(mar_zstd_color, 0.75),
        "MAR+ZSTD (no metadata)" = alpha(mar_zstd_color, 0.55),
        "MAR+ZSTD (dedup)" = alpha(mar_zstd_color, 0.90),
        "MAR+Gzip" = mar_gzip_color,
        "MAR+Gzip (no checksum)" = alpha(mar_gzip_color, 0.75),
        "MAR+Gzip (no metadata)" = alpha(mar_gzip_color, 0.55),
        "MAR+LZ4" = mar_lz4_color
      ),
      drop = FALSE
    ) +
    scale_x_continuous(expand = expansion(mult = c(0, 0.22))) +
    labs(title = title, subtitle = paste0(subtitle, run_note), x = xlab, y = NULL) +
    theme_tufte() +
    theme(legend.position = "none")

  if (!is.null(vline)) {
    p <- p + geom_vline(xintercept = vline, linetype = "dashed", color = "#2b6cb0", linewidth = 0.6)
  }

  ggsave(out_path, p, width = 14, height = 8)
}

# Helper function to create display label from variant
get_display_label <- function(variant) {
  case_when(
    variant == "tar_uncompressed" ~ "TAR (Uncompressed)",
    variant == "tar_gzip" ~ "TAR+Gzip",
    variant == "mar_uncompressed" ~ "MAR (Uncompressed)",
    variant == "mar_zstd" ~ "MAR+ZSTD",
    variant == "mar_gzip" ~ "MAR+Gzip",
    variant == "mar_lz4" ~ "MAR+LZ4",
    variant == "mar_zstd+no-checksum" ~ "MAR+ZSTD (no checksum)",
    variant == "mar_zstd+no-metadata" ~ "MAR+ZSTD (no metadata)",
    variant == "mar_zstd+dedup" ~ "MAR+ZSTD (dedup)",
    variant == "mar_gzip+no-checksum" ~ "MAR+Gzip (no checksum)",
    variant == "mar_gzip+no-metadata" ~ "MAR+Gzip (no metadata)",
    TRUE ~ variant
  )
}

