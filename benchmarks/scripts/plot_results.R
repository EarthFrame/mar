pareto_flag <- function(df) {
  # Pareto-optimal: no other point has >= ratio and <= time with at least one strict.
  df %>%
    group_by(workload) %>%
    mutate(
      pareto = map_lgl(seq_len(n()), function(i) {
        r <- ratio[[i]]
        t <- time_s[[i]]
        any_dom <- any((ratio >= r & time_s <= t) & (ratio > r | time_s < t), na.rm = TRUE)
        !any_dom
      })
    ) %>%
    ungroup()
}

plot_tradeoff <- function(df, title, out_path) {
  if (nrow(df) == 0) return()
  df <- pareto_flag(df)

  p <- ggplot(df, aes(x = ratio, y = time_s, color = display_label)) +
    geom_point(aes(shape = pareto), size = 3.2) +
    geom_text(aes(label = display_label), size = 2.6, vjust = -0.8, check_overlap = TRUE, color = "#222222") +
    facet_wrap(~facet_workload, scales = "free") +
    scale_color_manual(
      values = c(
        "TAR (Uncompressed)" = "#6e6e6e",
        "TAR+Gzip" = "#3d3d3d",
        "MAR (Uncompressed)" = mar_none_color,
        "MAR+ZSTD" = mar_zstd_color,
        "MAR+ZSTD (no checksum)" = alpha(mar_zstd_color, 0.75),
        "MAR+ZSTD (no metadata)" = alpha(mar_zstd_color, 0.55),
        "MAR+ZSTD (dedup)" = alpha(mar_zstd_color, 0.90),
        "MAR+Gzip" = mar_gzip_color,
        "MAR+Gzip (no checksum)" = alpha(mar_gzip_color, 0.75),
        "MAR+Gzip (no metadata)" = alpha(mar_gzip_color, 0.55),
        "MAR+LZ4" = mar_lz4_color
      ),
      drop = FALSE
    ) +
    scale_shape_manual(values = c(`TRUE` = 16, `FALSE` = 1), name = "Pareto") +
    scale_x_log10(labels = label_number(accuracy = 0.1)) +
    scale_y_log10(labels = label_number(accuracy = 0.1)) +
    labs(
      title = title,
      subtitle = paste0("Upper-right is better (more compression, less time). Points marked on the Pareto frontier are filled.", run_note),
      x = "Compression ratio (×, input_bytes / output_bytes)",
      y = "Time (s, log scale)"
    ) +
    theme_tufte() +
    theme(legend.position = "none")

  ggsave(out_path, p, width = 14, height = 8)
}

